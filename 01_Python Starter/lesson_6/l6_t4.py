# Створіть цілочисельний список, введіть кількість його елементів 
# і самі значення. Передбачити меню, в якому: після натискання 
# клавіші 1 ці значення виведуться на екран у зворотному порядку, 
# а після натискання клавіші 2 – за зростанням.


num_list = [int(input("Enter num: ")) for _ in range(int(input("Enter list size: ")))]

menu = input()
if menu == '1':
    print(num_list[::-1])
elif menu == '2':
    print(sorted(num_list))